#Program to display Value,Type,Memory Address for Variable using appropriate Built-in Functions

#Defining a variable
int_variable = 42

#Displaying the value of the variable
print("Value of my_variable:", int_variable) 

#Displaying the type of the variable
print("Type of my_variable:", type(int_variable))    
#Displaying the memory address of the variable
print("Memory address of my_variable:", id(int_variable))
