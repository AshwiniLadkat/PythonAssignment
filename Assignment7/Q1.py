#What is difference between local variable and global variable?
#ANS-
#A local variable is defined within a function and can only be accessed inside that function. 
# It is created when the function is called and destroyed when the function exits. 
# Local variables are used to store temporary data that is used within the context of the function.
#A global variable, on the other hand, is defined outside of any function and can be accessed from any part of the program, including inside functions. 
# Global variables are created when the program starts and exist until the program ends.
