#What is IndentationError?When does python raise IndentationError?
# IndentationError is a specific type of syntax error in Python that occurs 
# when the indentation of code blocks is not consistent or incorrect.
# Python uses indentation to define the structure and hierarchy of code blocks,
# such as loops, conditionals, functions, and classes. If the indentation is not    
# consistent, Python raises an IndentationError.


