#What is return value in a function?
#ANS-   It is value return by function after completion of its task.
def add(a, b):
    return a + b
result = add(3, 5)
print("The return value is:", result)  # Output: The return value is: 8
#In this example, the function `add` takes two parameters, `a` and `b`, 
# and returns their sum. The return value is then stored in the variable `result` and printed to the console.
